                        <p>
                            Цены на турпакеты в Эмираты поражают своей доступностью – в системе подбора тура отдельных туроператоров можно найти предложения менее чем за 600 долларов на двоих (6–7 ночей, отель 4*, без питания или с питанием ВВ). А незакрытые кресла есть даже на ближайшие вылеты. При этом в агентствах констатируют: заметного влияния на спрос это не оказывает, «погоду» в продажах по-прежнему делают туры в Египет. «В декабре по нашей сети было по несколько продаж в день по Эмиратам, сейчас же – когда как, но всплеска точно нет. Чаще всего покупают дорогие туры стоимостью 2–3 тыс. долларов», – констатирует гендиректор «Поехали с нами» Олег Кулик.
                        </p>
                        <p>
                            В туроператорских компаниях отмечают, что ситуация напоминает зиму 2012/2013, когда туры в Эмираты тоже зачастую реализовывались фактически по цене перелёта. Тогда происходящее связывали с завышенным объёмом перевозки, неадекватным спросу. Начальник отдела ОАЭ компании «Туристический Лидер» Александр Щекин напомнил, что сейчас этот фактор тоже есть: из Украины в Эмираты в общей сложности выполняется около 30 еженедельных рейсов (МАУ, Air Arabia, Flydubai). Кроме того, по мнению экспертов, свою роль играет и переориентация туристов на другие направления, которые тоже в той или иной степени подвержены «горению». Директор «Натали Турс Украина» Алексей Корнеев обращает внимание на то, что причиной ценопада стал целый комплекс факторов, в том числе аналогичные явления на египетском рынке, низкий сезон и продолжающаяся девальвация гривны, влекущая за собой снижение покупательской способности населения. «В основном клиентуру перетягивает Египет, который тоже «горит» даже после многочисленных оптимизаций полётных программ», – уточняет директор «Зевс Тревел» Юлия Тараненко. Причём «погасить» пожар спецпредложений по ОАЭ, где перевозка строится на регулярных рейсах, будет не так просто, как на египетском направлении, где можно снять часть чартеров. «В случае с Эмиратами так не получится, и сложнее всего придётся компаниям, у которых большие блоки мест», – заключает собеседница.
                        </p>
                        <p>
                            Как долго продлится такая ситуация, прогнозировать сложно. По словам собеседников, в том числе это будет зависеть от того, как быстро войдёт «в тонус» ценообразование по Египту. До тех пор, пока агенты получают рассылку наподобие «Хургада на 7 ночей от 149 $ + 2 % комиссии», им сложно будет мотивировать туристов заплатить вдвое больше за тур в Эмираты.
